package com.uestc.bean;

public class logistics {
	
	private int l_id;
	
	private String g_name;
	private String g_barcode;
	private String l_num;
	private String l_price;
	public int getL_id() {
		return l_id;
	}
	public void setL_id(int l_id) {
		this.l_id = l_id;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_barcode() {
		return g_barcode;
	}
	public void setG_barcode(String g_barcode) {
		this.g_barcode = g_barcode;
	}
	public String getL_num() {
		return l_num;
	}
	public void setL_num(String l_num) {
		this.l_num = l_num;
	}
	public String getL_price() {
		return l_price;
	}
	public void setL_price(String l_price) {
		this.l_price = l_price;
	}
	
	
	
	
}
